public class Relation {
    String vid;
    String uhd_url;
    String hd_url;
    String sd_url;
    String vname;
    String category;
    String posturl;
    String show_times;

    public String getVid() {
        return vid;
    }

    public void setVid(String vid) {
        this.vid = vid;
    }

    public String getUhd_url() {
        return uhd_url;
    }

    public void setUhd_url(String uhd_url) {
        this.uhd_url = uhd_url;
    }

    public String getHd_url() {
        return hd_url;
    }

    public void setHd_url(String hd_url) {
        this.hd_url = hd_url;
    }

    public String getSd_url() {
        return sd_url;
    }

    public void setSd_url(String sd_url) {
        this.sd_url = sd_url;
    }

    public String getVname() {
        return vname;
    }

    public void setVname(String vname) {
        this.vname = vname;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public String getPosturl() {
        return posturl;
    }

    public void setPosturl(String posturl) {
        this.posturl = posturl;
    }

    public String getShow_times() {
        return show_times;
    }

    public void setShow_times(String show_times) {
        this.show_times = show_times;
    }
}
